<?php

use Illuminate\Support\Facades\Route;
use App\Http\Controllers\ObjectController;
use App\Http\Controllers\ImageController;
use App\Http\Controllers\ProfileController;
use App\Http\Controllers\CompareController;

/*
|--------------------------------------------------------------------------
| Web Routes
|--------------------------------------------------------------------------
*/

Route::get('/', [ObjectController::class, 'home'])->name('home');
Route::get('/board', [ObjectController::class, 'board'])->name('board');
Route::get('/objects/{id}', [ObjectController::class, 'show'])->name('objects.show');

Route::middleware(['auth'])->group(function () {
    Route::get('/profile', [ProfileController::class, 'edit'])->name('profile.edit');
    Route::post('/profile', [ProfileController::class, 'update'])->name('profile.update');

    Route::get('/upload', [ImageController::class, 'create'])->name('images.create');
    Route::post('/upload', [ImageController::class, 'store'])->name('images.store');
    Route::get('/compare/{objectId}', [CompareController::class, 'show'])->name('compare.show');

    // Dashboard view for logged-in users
    Route::get('/dashboard', function () {
        return view('dashboard');
    })->name('dashboard');
});

/*
 | Include authentication routes (generated by Breeze / Laravel UI)
 | this will register routes like login, register, logout, etc.
*/
if (file_exists(__DIR__ . '/auth.php')) {
    require __DIR__ . '/auth.php';
}

use App\Http\Controllers\Admin\UserController as AdminUserController;
use App\Http\Controllers\Admin\ImageController as AdminImageController;

Route::prefix('admin')->middleware(['auth','is_admin'])->group(function () {
    Route::get('/', function(){ return redirect()->route('admin.users.index'); });

    // users
    Route::get('/users', [AdminUserController::class,'index'])->name('admin.users.index');
    Route::get('/users/{user}/edit', [AdminUserController::class,'edit'])->name('admin.users.edit');
    Route::put('/users/{user}', [AdminUserController::class,'update'])->name('admin.users.update');
    Route::delete('/users/{user}', [AdminUserController::class,'destroy'])->name('admin.users.destroy');

    // images
    Route::get('/images', [AdminImageController::class,'index'])->name('admin.images.index');
    Route::get('/images/{image}/edit', [AdminImageController::class,'edit'])->name('admin.images.edit');
    Route::put('/images/{image}', [AdminImageController::class,'update'])->name('admin.images.update');
    Route::delete('/images/{image}', [AdminImageController::class,'destroy'])->name('admin.images.destroy');
    Route::post('/images/{image}/approve', [AdminImageController::class,'approve'])->name('admin.images.approve');
});
